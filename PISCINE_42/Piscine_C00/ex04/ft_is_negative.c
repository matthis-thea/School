#include <unistd.h>

void	ft_is_negative(int n);

void	ft_is_negative(int n)
{
	char	i;

	if (n >= 0)
	{
		i = 'P';
		write(1, &i, 1);
	}
	else
	{
		i = 'N';
		write(1, &i, 1);
	}
}
